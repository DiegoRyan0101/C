#include <stdio.h>
//Operadores Matemáticos
int main()
{
    float soma, subtracao, multiplicacao, divisao;
    soma            = 7 + 9;
    subtracao       = 5 - 5;
    multiplicacao   = 12 * 2;
    divisao         = 12 / 2;
    printf("Soma: %.2f\n", soma);
    printf("Subtração: %.2f\n", subtracao);
    printf("Multiplicação: %.2f\n", multiplicacao);
    printf("Divisão: %.2f\n", divisao);
    return 0;

}
